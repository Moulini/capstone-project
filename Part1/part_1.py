# Drop categorical column for matrix math
numeric_df = df.select_dtypes(include=[np.number])

pearson_mat = numeric_df.corr(method='pearson')
spearman_mat = numeric_df.corr(method='spearman')

# Calculate absolute differences
diff_mat = (spearman_mat - pearson_mat).abs()

# Extract unique pairs (lower triangle) to find top differences
upper_tri = diff_mat.where(np.triu(np.ones(diff_mat.shape), k=1).astype(bool))
diff_pairs = upper_tri.unstack().dropna().sort_values(ascending=False)

print("\nTop 3 Pairs with Largest Absolute Difference |Spearman - Pearson|:")
print(f"1. {diff_pairs.index[0]}: {diff_pairs.iloc[0]:.4f}")
print(f"2. {diff_pairs.index[1]}: {diff_pairs.iloc[1]:.4f}")
print(f"3. {diff_pairs.index[2]}: {diff_pairs.iloc[2]:.4f}\n")


