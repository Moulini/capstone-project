# Identify the pair with the highest absolute correlation (excluding self-correlation)
# Unstack the matrix, remove self-correlations, and grab the absolute max
corr_unstacked = corr_matrix.abs().unstack()
corr_pairs_sorted = corr_unstacked[corr_unstacked < 1.0].sort_values(ascending=False)

# Get the top pair
highest_pair = corr_pairs_sorted.index[0]
highest_val = corr_matrix.loc[highest_pair[0], highest_pair[1]]

print(f"Highest absolute correlation pair: {highest_pair[0]} and {highest_pair[1]}")
print(f"Pearson Correlation Coefficient: {highest_val:.4f}")

